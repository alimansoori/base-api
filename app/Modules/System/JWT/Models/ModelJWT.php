<?php
namespace Modules\System\JWT\Models;

class ModelJWT extends \Lib\Mvc\Model
{
    private $userid;
    private $content;

    protected function init()
    {
        $this->setSource('jwt');
    }

    /**
     * @return mixed
     */
    public function getUserid()
    {
        return $this->userid;
    }

    /**
     * @param mixed $userid
     */
    public function setUserid($userid)
    {
        $this->userid = $userid;
    }

    /**
     * @return mixed
     */
    public function getContent()
    {
        return $this->content;
    }

    /**
     * @param mixed $content
     */
    public function setContent($content)
    {
        $this->content = $content;
    }
}